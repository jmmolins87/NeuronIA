"use client"

import * as React from "react"
import * as AlertDialogPrimitive from "@radix-ui/react-alert-dialog"

import { cn } from "@/lib/utils"
import { Button } from "@/components/ui/button"
import { lockScroll } from "@/lib/scroll-lock"

function AlertDialogScrollLockEffect() {
  React.useEffect(() => {
    const unlock = lockScroll()
    return () => unlock()
  }, [])

  return null
}

function AlertDialog({
  ...props
}: React.ComponentProps<typeof AlertDialogPrimitive.Root>) {
  return <AlertDialogPrimitive.Root data-slot="alert-dialog" {...props} />
}

function AlertDialogTrigger({
  ...props
}: React.ComponentProps<typeof AlertDialogPrimitive.Trigger>) {
  return (
    <AlertDialogPrimitive.Trigger data-slot="alert-dialog-trigger" {...props} />
  )
}

function AlertDialogPortal({
  ...props
}: React.ComponentProps<typeof AlertDialogPrimitive.Portal>) {
  return (
    <AlertDialogPrimitive.Portal data-slot="alert-dialog-portal" {...props} />
  )
}

function AlertDialogOverlay({
  className,
  ...props
}: React.ComponentProps<typeof AlertDialogPrimitive.Overlay>) {
  const ref = React.useCallback(
    (node: HTMLDivElement | null) => {
      if (!node) return
      if (process.env.NODE_ENV === "production") return

      const cs = window.getComputedStyle(node)
      console.info("[ui] AlertDialogOverlay mounted", {
        className,
        computed: {
          backgroundColor: cs.backgroundColor,
          opacity: cs.opacity,
          backdropFilter: cs.backdropFilter,
          webkitBackdropFilter: (cs as unknown as { webkitBackdropFilter?: string })
            .webkitBackdropFilter,
        },
      })
    },
    [className]
  )

  return (
    <AlertDialogPrimitive.Overlay
      data-slot="alert-dialog-overlay"
      data-overlay-version="ui-overlay-2026-02-07"
      ref={ref}
      className={cn(
        "data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0 fixed inset-0 z-50 bg-transparent backdrop-blur-none overscroll-contain touch-none",
        className
      )}
      {...props}
    />
  )
}

function AlertDialogContent({
  className,
  size = "default",
  overlayClassName,
  children,
  ...props
}: React.ComponentProps<typeof AlertDialogPrimitive.Content> & {
  size?: "default" | "sm" | "lg"
  overlayClassName?: string
}) {
  return (
    <AlertDialogPortal>
      <AlertDialogOverlay className={overlayClassName} />
      <AlertDialogPrimitive.Content
        data-slot="alert-dialog-content"
        data-size={size}
        data-lenis-prevent
        className={cn(
          "bg-background/85 supports-[backdrop-filter]:bg-background/65 backdrop-blur-xl backdrop-saturate-150 data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0 data-[state=closed]:zoom-out-95 data-[state=open]:zoom-in-95 group/alert-dialog-content fixed top-[50%] left-[50%] z-50 grid w-full max-w-[calc(100%-2rem)] translate-x-[-50%] translate-y-[-50%] gap-4 rounded-lg border p-6 shadow-lg duration-200 data-[size=sm]:max-w-xs data-[size=default]:sm:max-w-lg data-[size=lg]:sm:max-w-2xl max-h-[calc(100dvh-2rem)] overflow-y-auto overscroll-contain touch-auto [-webkit-overflow-scrolling:touch]",
          className
        )}
        {...props}
      >
        <AlertDialogScrollLockEffect />
        {children}
      </AlertDialogPrimitive.Content>
    </AlertDialogPortal>
  )
}

